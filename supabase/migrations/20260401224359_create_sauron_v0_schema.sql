-- 2. BRIEFS — Daily command briefs generated by Sauron
CREATE TABLE sauron.briefs (
    id              BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    brief_date      DATE NOT NULL DEFAULT CURRENT_DATE,
    brief_type      TEXT NOT NULL DEFAULT 'daily',  -- daily, weekly, monthly, ad_hoc
    content         TEXT NOT NULL,                   -- The actual brief text
    data_snapshot   JSONB,                           -- Raw data that informed the brief
    action_items    JSONB,                           -- Extracted action items
    delivered_via   TEXT,                             -- 'slack', 'sms', 'email'
    delivered_at    TIMESTAMPTZ,
    acknowledged_at TIMESTAMPTZ,                     -- When Dez opened/responded
    created_at      TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(brief_date, brief_type)
);

-- 3. ACTION_ITEMS — Things Sauron surfaced that need attention
CREATE TABLE sauron.action_items (
    id              BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    business_id     TEXT REFERENCES sauron.businesses(id),
    brief_id        BIGINT REFERENCES sauron.briefs(id),
    priority        TEXT DEFAULT 'normal',  -- critical, high, normal, low
    zone            INTEGER DEFAULT 1,      -- 1=auto, 2=log, 3=needs_approval, 4=human_only
    title           TEXT NOT NULL,
    description     TEXT,
    status          TEXT DEFAULT 'open',     -- open, approved, vetoed, done, expired
    due_by          TIMESTAMPTZ,
    resolved_at     TIMESTAMPTZ,
    created_at      TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_action_items_status ON sauron.action_items (status) WHERE status = 'open';
CREATE INDEX idx_action_items_business ON sauron.action_items (business_id);

-- 4. METRICS_LOG — Daily KPI snapshots for trend tracking
CREATE TABLE sauron.metrics_log (
    id              BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    business_id     TEXT REFERENCES sauron.businesses(id),
    metric_date     DATE NOT NULL DEFAULT CURRENT_DATE,
    metric_name     TEXT NOT NULL,
    metric_value    NUMERIC,
    metric_unit     TEXT,           -- 'dollars', 'count', 'percent', 'seconds'
    notes           TEXT,
    created_at      TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(business_id, metric_date, metric_name)
);

CREATE INDEX idx_metrics_business_date ON sauron.metrics_log (business_id, metric_date DESC);

-- 5. DATA_SOURCES — What Sauron checks each morning
CREATE TABLE sauron.data_sources (
    id              TEXT PRIMARY KEY,
    business_id     TEXT REFERENCES sauron.businesses(id),
    source_name     TEXT NOT NULL,
    source_type     TEXT NOT NULL,     -- 'supabase', 'google_ads', 'jobber', 'manual'
    query_config    JSONB,             -- How to query this source
    check_frequency TEXT DEFAULT 'daily',
    active          BOOLEAN DEFAULT TRUE,
    last_checked    TIMESTAMPTZ,
    created_at      TIMESTAMPTZ DEFAULT NOW()
);

