CREATE SCHEMA IF NOT EXISTS sauron;

-- 1. BUSINESSES — The entities Sauron manages
CREATE TABLE sauron.businesses (
    id              TEXT PRIMARY KEY,
    name            TEXT NOT NULL,
    legal_name      TEXT,
    business_type   TEXT,       -- 'acquisitions', 'service'
    active          BOOLEAN DEFAULT TRUE,
    created_at      TIMESTAMPTZ DEFAULT NOW()
);

